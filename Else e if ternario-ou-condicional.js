
/*
Operador ternário
? = If (se)
: = Else (se não)


*/
// exemplo:
const salario = 5000
if (salario < 6000) {

    console.log("O colaborador é junior")
}
else {

    console.log("O colaborador é senior")
}


/*
No exemplo acima eu posso economizar codigo fazendo da seguinte maneira =
*/
salario < 6000 ?  console.log("O colaborador é junior") : console.log("O colaborador é senior")
