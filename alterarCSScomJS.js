/*Como alterar o css ou os estilos do site usando o javascript? */

//                                  Exemplo:
const button = document.querySelector(".BotãoDeClique") //Vou estilizar uma classe.

button.style.color = "white" // Estou substituindo a cor das letras de dentro do button.
button.style.backgroundColor = "#682a57" // Estou substituindo a cor da área interna do botão.
  



//                     Outro exemplo:

// vou trocar o tamanho da fonte de escrita do meu h1
const h1 = document.querySelector("h1") //Vou estilizar uma tag o h1.

h1.style.fontSize = ("80px") //Vou alterar o tamnho das letras usando o js.