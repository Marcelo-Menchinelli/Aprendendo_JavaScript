/*

Switch Case - Controlador de fluxo

*/

const user = "Marcelo"

switch (user) {
    case "João":
        console.log("É o joao")
        break

    case "Marcelo":
        console.log("É o marcelo")
        break

    case "José":
        console.log("É o jose")
        break

    default:
        console.log("Nenhum do valores")
        break
}